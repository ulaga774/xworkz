public class ReturnTypes{
	public static void main(String[] args) {
		
		
		byte marks = tamil();
		System.out.println(marks);
		
		Short bikeNumber = tvs();
		System.out.println(bikeNumber);
		
		int bank = money();
		System.out.println(bank);
		
		long carPrice = cost();
		System.out.println(carPrice);
		
		float value = ownMethod();
		System.out.println(value);
		
		double ratio = maths();
		System.out.println(ratio);
		
		char mark = grade();
		System.out.println(marks);
		
		boolean asLaptopWorking = myMethod();
		System.out.println(asLaptopWorking);
		
		String kiran = names();
		System.out.println("kiran");
	
	}
	public static byte tamil(){
		return 6;
	}
	public static Short tvs(){
		return 6874;
	}
	public static int money(){
		return 62;
	}
	public static long cost(){
		return 6;
	}
	public static float ownMethod(){
		return 32;
	}
	public static double maths(){
		return 5;
	}
	public static char grade(){
		return 'b' ;
	}
	public static boolean myMethod(){
		return true;
	}
	public static String names(){
		return "varun";	
		
		
	}
	
}
	
	
	